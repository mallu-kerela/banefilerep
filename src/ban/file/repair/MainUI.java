    private void removeYValuesPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        basicInit();
        if(banFiles.size() == 0)
            status.setText("Zero BAN Files Present");
        else
        {
            HelperClass.removeYvalues(banFiles);
            status.setText("Complete");
            fileStatusUpdate();
        }
        
    }

    private void incValuePerformed(ActionEvent e) {

        if(incDecTextField.getText().equals(""))
            return;

        basicInit();
        if(banFiles.size() == 0)
            status.setText("Zero BAN Files Present");
        else
        {
            Double value = Double.parseDouble(incDecTextField.getText());
            HelperClass.incrementByValues(banFiles,value);
            status.setText("Complete");
            fileStatusUpdate();
        }
    }

    private void fileStatusUpdate()
    {
        String data = "";
        for(int i=0; i<filesOperated.size(); i++)
            data = data + filesOperated.get(i).getName() + "\n";
        textArea.setText(data);
    }

    private void basicInit()
    {
       status.setText("Processing"); 
       directory = System.getProperty("user.dir");
       banFiles = HelperClass.getBANFiles(directory);
       
       if(ascendingButton.isSelected())
           sequencerMode = 1;
       if(descendingButton.isSelected())
           sequencerMode = 2;
       
    }
    
    
