    private void fileStatusUpdate()
    {
        String data = "";
        for(int i=0; i<filesOperated.size(); i++)
            data = data + filesOperated.get(i).getName() + "\n";
        textArea.setText(data);
    }

    private void basicInit()
    {
       status.setText("Processing"); 
       directory = System.getProperty("user.dir");
       banFiles = HelperClass.getBANFiles(directory);
       
       if(ascendingButton.isSelected())
           sequencerMode = 1;
       if(descendingButton.isSelected())
           sequencerMode = 2;
       
    }
    
    
